public class Calculadora {
	/**
	 * Este metodo é utilizado para somar dois inteiros 
	 * @param numeroUm este é o primeiro atributo
	 * @param numeroDois este é o segundo atributo
	 * @return int o resultado deste metodo é a soma dos dois inteiros
	 */
	public int somar(int numeroUm, int numeroDois) {
		return numeroUm + numeroDois;
	}

}
